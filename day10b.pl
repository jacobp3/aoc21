%brackets =  ( "(" => ")",
	           "[" => "]", 
    	       "{" => "}", 
    	       "<" => ">");

@results = ();

foreach $l (@lines) {
    @stack = ();
    foreach $c (split(//, $l)) {
        if(exists($brackets{$c})) {
            push(@stack,($c));
        } else {
            $o = pop(@stack);
            if($c ne $brackets{$o}) {
                next;
            }
        }
    }
    $length = scalar @stack;
    if($length > 0) {
        $result = 0;
        while($o = pop(@stack)) {
            if($o eq "{") {
                $result = $result * 5 + 3;
            } elsif($o eq "[") {
                $result = $result * 5 + 2;
            } elsif($o eq "(") {
                $result = $result * 5 + 1;
            } elsif($o eq "<") {
                $result = $result * 5 + 4;
            }
        }
        push(@results, ($result));
    }
}

$numResults = scalar @results;
@sorted = sort { $a <=> $b } @results;

print "@sorted\n";
print "$sorted[$numResults / 2]\n";
