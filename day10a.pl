@stack = ();
$result = 0;

foreach $l (@lines) {
    foreach $c (split(//, $l)) {
        if(exists($brackets{$c})) {
            push(@stack,($c));
        } else {
            $o = pop(@stack);
            if($c ne $brackets{$o}) {
                if($c eq "}") {
                    $result = $result + 1197;
                } elsif($c eq "]") {
                    $result = $result + 57;
                } elsif($c eq ")") {
                    $result = $result + 3;
                } elsif($c eq ">") {
                    $result = $result + 25137;
                }
            }
        }
    }
}
